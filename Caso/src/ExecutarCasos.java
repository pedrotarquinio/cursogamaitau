
public class ExecutarCasos {

	public static void main(String[] args) {
		//Criando uma variável
		int dia;
		
		//Incrementando a variável
		
		dia = 6;
		
		//Montando uma estrutura de casos
		switch(dia) {
		case 1: 
			System.out.println("Olá, hoje é domingo");
			break;
		case 2:
			System.out.println("Olá, hoje é segunda");
			break;
		case 3:
			System.out.println("Olá, hoje é terça");
			break;
		}
	}
}
